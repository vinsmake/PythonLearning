text = "0123456789"

#extracts the character number 2
print(text[2])

#slices every character starting from 4
print(text[4:])

#slices every character between 2 and 6
print(text[2:6])

#extracts every character every 3 characters
print(text[::3])

#slices every character between 1 and 6 every 3 characters
print(text[1:9:3])

